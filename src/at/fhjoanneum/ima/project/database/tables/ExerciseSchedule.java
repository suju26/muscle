package at.fhjoanneum.ima.project.database.tables;

public class ExerciseSchedule {
	private long id;
	private long fkSchedule;
	private long day;
	private long position;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getFkSchedule() {
		return fkSchedule;
	}

	public void setFkSchedule(long fkSchedule) {
		this.fkSchedule = fkSchedule;
	}

	public long getDay() {
		return day;
	}

	public void setDay(long day) {
		this.day = day;
	}

	public long getPosition() {
		return position;
	}

	public void setPosition(long position) {
		this.position = position;
	}

}
